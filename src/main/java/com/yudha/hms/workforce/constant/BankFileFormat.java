package com.yudha.hms.workforce.constant;

/**
 * Indonesian Bank File Format Types.
 *
 * @author HMS Development Team
 * @version 1.0.0
 * @since 2025-01-22
 */
public enum BankFileFormat {
    /**
     * Standard CSV format (universal)
     */
    STANDARD_CSV,

    /**
     * BCA (Bank Central Asia) format
     */
    BCA,

    /**
     * Bank Mandiri format
     */
    MANDIRI,

    /**
     * BNI (Bank Negara Indonesia) format
     */
    BNI,

    /**
     * BRI (Bank Rakyat Indonesia) format
     */
    BRI
}
